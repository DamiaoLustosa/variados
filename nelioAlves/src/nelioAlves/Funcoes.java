package nelioAlves;

public class Funcoes {
	public static void main(String args[]) {

		// Funções em java tem o mesmo significado de métodos

		imprimeValor(); // chamando um método/função fora do método principal

	}

	// Iniciando um método diferente do metodo principal

	public static void imprimeValor() {
		System.out.println("Imprimindo por meio de método!");

	}

}
